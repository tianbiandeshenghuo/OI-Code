#include<bits/stdc++.h>

using namespace std;
int t, n, m;
void Solve(){
  cin >> n >> m;
  int a[n + 1][m + 1];
  for (int i = 1; i <= n; i++){
    for (int j = 1; j <= m; j++){
      cin >> a[i][j];
    }
  }
  
}
int main(){
  ios::sync_with_stdio(0), cin.tie(0), cout.tie(0);
  cin >> t;
  while (t--){
    Solve();
  }
  return 0;
}