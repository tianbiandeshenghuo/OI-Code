#include<bits/stdc++.h>

using namespace std;
const int MAXN = 1e5 + 5;
int n, m, a[MAXN];
void Solve(){
  cin >> n >> m;
  for (int i = 1; i <= m; i++){
    cin >> a[i];
  }
  
}
int main(){
  ios::sync_with_stdio(0), cin.tie(0), cout.tie(0);
  int t;
  cin >> t;
  while (t--){
    Solve();
  }
  return 0;
}